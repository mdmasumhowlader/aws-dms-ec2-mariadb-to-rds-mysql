CREATE TABLE BUSINESS_UNITS (
  BUSINESS_UNIT_ID INT PRIMARY KEY,
  BUSINESS_UNIT_NAME VARCHAR(255)
);

CREATE TABLE CL_CATEGORIES (
  CL_CATEGORY_ID INT PRIMARY KEY,
  CL_CODE VARCHAR(20),
  DESCRIPTION VARCHAR(200)
);

CREATE TABLE CUSTOMER_TYPES (
  CUSTOMER_TYPE_ID INT PRIMARY KEY,
  CUSTOMER_TYPE VARCHAR(40),
  DESCRIPTION VARCHAR(500)
);

CREATE TABLE DIVISIONS (
  DIVISION_ID INT PRIMARY KEY,
  DIVISION_NAME VARCHAR(150)
);

CREATE TABLE INDUSTRIES (
  INDUSTRY_ID INT PRIMARY KEY,
  SECTOR_NAME VARCHAR(100),
  SUB_SECTOR_NAME VARCHAR(100),
  RISK_CATEGORY VARCHAR(20)
);

CREATE TABLE CUSTOMERS (
  CUSTOMER_ID INT PRIMARY KEY,
  CUSTOMER_TYPE_ID INT,
  CUSTOMER_NAME VARCHAR(200),
  CUSTOMER_RISK_LEVEL VARCHAR(30),
  LAST_UPDATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  ADDRESS VARCHAR(500),
  EMAIL VARCHAR(50),
  MOBILE VARCHAR(20),
  BRANCH_ID INT
);

CREATE TABLE DISTRICTS (
  DISTRICT_ID INT PRIMARY KEY,
  DISTRICT_NAME VARCHAR(150),
  DIVISION_ID INT
);

CREATE TABLE THANAS (
  THANA_ID INT PRIMARY KEY,
  THANA_NAME VARCHAR(150),
  DISTRICT_ID INT
);

CREATE TABLE BRANCHES (
  BRANCH_ID INT PRIMARY KEY,
  BRANCH_NAME VARCHAR(150),
  ADDRESS VARCHAR(765),
  THANA_ID INT,
  LAST_UPDATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE FINANCING_PRODUCTS (
  PRODUCT_ID INT PRIMARY KEY,
  PRODUCT_CODE VARCHAR(30),
  PRODUCT_NAME VARCHAR(150),
  PRODUCT_TYPE VARCHAR(50),
  INSTALLMENT_BASED TINYINT,
  INDUSTRY_ID INT,
  LAST_UPDATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PERSONAL_FINANCING TINYINT DEFAULT 0
);

CREATE TABLE FINANCING_ACCOUNTS (
    ACCOUNT_ID BIGINT NOT NULL AUTO_INCREMENT,

    CUSTOMER_ID INT,
    BRANCH_ID INT,
    BUSINESS_UNIT_ID INT,
    PRODUCT_ID INT,
    CL_CATEGORY_ID INT,

    ACCOUNT_NO VARCHAR(20),
    OPEN_DATE DATE,
    EXPIRY_DATE DATE,
    CLOSE_DATE DATE,

    ACCOUNT_STATUS VARCHAR(20) DEFAULT 'ACTIVE',

    SANCTION_AMOUNT DECIMAL(18,2),
    OUTSTANDING_AMT DECIMAL(18,2),
    PRINCIPAL_BALANCE DECIMAL(18,2),
    ACC_PROFIT_RATE DECIMAL(10,2),

    IS_INSTALLMENT TINYINT(1) DEFAULT 0,
    CL_STATUS VARCHAR(10),

    CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP,
    LAST_UPDATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        ON UPDATE CURRENT_TIMESTAMP,

    PRIMARY KEY (ACCOUNT_ID),

    CONSTRAINT FIN_ACC_STATUS_CHK
        CHECK (ACCOUNT_STATUS IN ('ACTIVE','CLOSED','DORMANT')),

    CONSTRAINT FK_FIN_ACC_CUSTOMER
        FOREIGN KEY (CUSTOMER_ID)
        REFERENCES CUSTOMERS(CUSTOMER_ID),

    CONSTRAINT FK_FIN_ACC_BU
        FOREIGN KEY (BUSINESS_UNIT_ID)
        REFERENCES BUSINESS_UNITS(BUSINESS_UNIT_ID),

    CONSTRAINT FK_FIN_ACC_CL
        FOREIGN KEY (CL_CATEGORY_ID)
        REFERENCES CL_CATEGORIES(CL_CATEGORY_ID),

    CONSTRAINT FK_FIN_ACC_PRODUCT
        FOREIGN KEY (PRODUCT_ID)
        REFERENCES FINANCING_PRODUCTS(PRODUCT_ID),

    CONSTRAINT FK_FIN_ACC_BRANCH
        FOREIGN KEY (BRANCH_ID)
        REFERENCES BRANCHES(BRANCH_ID)
) ENGINE=InnoDB;


CREATE TABLE FINANCING_MONTHLY_STATEMENT (
  ACCOUNT_ID INT PRIMARY KEY,
  CUSTOMER_ID INT,
  BRANCH_ID INT,
  BUSINESS_UNIT_ID INT,
  PRODUCT_ID INT,
  CL_CATEGORY_ID INT,
  ACCOUNT_NO VARCHAR(20),
  OPEN_DATE DATE,
  EXPIRY_DATE DATE,
  CLOSE_DATE DATE,
  ACCOUNT_STATUS VARCHAR(20),
  SANCTION_AMOUNT DECIMAL(18,2),
  OUTSTANDING_AMT DECIMAL(18,2),
  PRINCIPAL_BALANCE DECIMAL(18,2),
  ACC_PROFIT_RATE DECIMAL(10,2),
  IS_INSTALLMENT TINYINT,
  CL_STATUS VARCHAR(10),
  CREATED_AT DATETIME,
  LAST_UPDATE_TIME DATETIME,
  YEAR INT,
  MONTH INT
);



